//Write a program using if else if ladder

public class if_else_if17 {
     public static void main(String[] args) {
        int marks = 85;
        if(marks >= 90) {
            System.out.println("A");
        } else if(marks >= 75) {
            System.out.println("B");
        } else if(marks >= 60) {
            System.out.println("C");
        } else {
            System.out.println("D");
        }
    }
}
