//Write a program on nested if statement

public class nested_if18 {
    public static void main(String[] args) {
        int number = 10;
        if(number > 0) {
            if(number % 2 == 0) {
                System.out.println("Positive Even Number");
            } else {
                System.out.println("Positive Odd Number");
            }
        }
    }
}
